#include <stdio.h>

int main (void) {
    
    // putchar allows us to print ONE character to the terminal at a time
    putchar('a');
    
    // adding 5 to 'a' adds it to the ascii value, resulting in 'f'
    putchar('a' + 5);
    
    // the decimal value of an ascii character can be printed out
    printf("%d", ' ');

    return 0;
}
